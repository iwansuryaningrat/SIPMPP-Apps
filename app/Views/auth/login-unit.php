    <script>
        $(document).ready(function() {
            $("#tahun").change(function() {
                var tahun = $(this).val();
                $.ajax({
                    url: "<?= base_url('auth/getrole/' . $email) ?>",
                    type: "POST",
                    data: {
                        tahun: tahun,
                    },
                    dataType: "json",
                    success: function(response) {
                        $("#role").html(response);
                    },
                });
            });
            $("#role").change(function() {
                var id = $(this).val();
                var tahun = $('#tahun').val();
                $.ajax({
                    url: "<?= base_url('auth/getunit/' . $email) ?>",
                    type: "POST",
                    data: {
                        role_id: id,
                        tahun: tahun,
                    },
                    dataType: "json",
                    success: function(response) {
                        $("#unit").html(response);
                    },
                });
            });
        });
